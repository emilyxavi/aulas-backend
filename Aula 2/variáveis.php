
1<h2>Variáveis</h2>

<p>são estruturas (memória utilizada) para armazenar dados que serão processados pelo programa</p>
<p> 
    Para criar uma variável em PHP basta colocar
     simbolos de $ na frente de um nome e atribuir um valor com o simbolo de igual. Exemplo:

     <pre>
     $variavel = 10;
     $nome = "Maria";
     $nota = 5.5;
</pre>
</p>
