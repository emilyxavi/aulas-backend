<h2>Variáveis</h2>
<p>São estruturas que armazenam dados na memória.</p>
<p>Para criar uma variável em PHP basta coloocar símbolo de $ na frente de um nome e atribuir  um valor com o simbolo de igual. Exemplo:
    <pre>
    $variavel=10;
    $nome="Maria";
    $nota=5.5;
    </pre>
</p>
<?php
    $nome=" Ivan";
    echo "Olá, $nome. Seja bem-vindo a aula de backend."
?>